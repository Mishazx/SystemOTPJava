#!/bin/bash

# Этот скрипт запускает приложение, загружая переменные окружения из .env файла

# Проверяем наличие .env файла
if [ ! -f .env ]; then
    echo "ОШИБКА: Файл .env не найден!"
    echo "Пожалуйста, создайте .env файл на основе .env.example"
    exit 1
fi

# Загружаем переменные из .env файла
export $(grep -v '^#' .env | xargs)

# Запускаем приложение с переменными окружения
./gradlew bootRun

# Очищаем переменные окружения после завершения
unset $(grep -v '^#' .env | sed -E 's/(.*)=.*/\1/' | xargs) 